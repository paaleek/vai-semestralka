<?php

namespace App\Actions\Fortify;

use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Support\Facades\Validator;
use Illuminate\Validation\Rule;
use Laravel\Fortify\Contracts\UpdatesUserProfileInformation;

class UpdateUserProfileInformation implements UpdatesUserProfileInformation
{
    /**
     * Validate and update the given user's profile information.
     *
     * @param  mixed  $user
     * @param  array  $input
     * @return void
     */
    public function update($user, array $input)
    {
        Validator::make($input, [
            'name' => ['required', 'string', 'max:255'],

            'email' => [
                'required',
                'string',
                'email',
                'max:255',
                Rule::unique('users')->ignore($user->id),
            ],
            'profile_picture' => ['nullable', 'image'],
        ])->validate();

        if (request()->hasFile('profile-picture')) {
            $file = request()->file('profile-picture');
            $filename = date('Y-m-d-H_i_s').'_'.$file->getClientOriginalName();


            $file->move(public_path('img/users/'), $filename);
            $user['profile_picture'] = $filename;
        }

        if ($input['email'] !== $user->email &&
            $user instanceof MustVerifyEmail) {
            $this->updateVerifiedUser($user, $input);
        } else {
            $user->forceFill([
                'name' => $input['name'],
                'email' => $input['email'],
            ])->save();
        }
    }

    protected function uploadProfilePicture($input) {
        if (request()->hasFile('profile_picture')) {
            $file = request()->file('profile_picture');
            $filename = request()->profile_picture->getClientOriginalName();

